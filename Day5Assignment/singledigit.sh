#!/usr/bin/bash
read -p "enter the number" no

if [ $no == 1 ]
then
 echo "one"
elif [ $no == 2 ]
then
echo "two"
elif [ $no == 3 ]
then
echo "three"
elif [ $no == 4 ]
then
 echo "four"
elif [ $no == 5 ]
then
 echo "five"
elif [ $no == 6 ]
then
 echo "six"
elif [ $no == 7 ]
then
 echo "seven"
elif [ $no == 8 ]
then
 echo "eight"
elif [ $no == 9 ]
then
 echo "nine"
else
 echo "number is not single digit"
fi
